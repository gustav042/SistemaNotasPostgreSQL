    private void jButton13ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton13ActionPerformed
        try {
            // Creating a PdfDocument object
            Aluno aluno = sistema.selecionaAluno();
            String dest =  aluno.getNome().charAt(0) + ".pdf";
            PdfWriter writer;
            writer = new PdfWriter(dest);
            // Creating a PdfDocument object
            PdfDocument pdf = new PdfDocument(writer);
            // Creating a Document object
            Document doc = new Document(pdf);
            // Creating a table
            doc.add(new Paragraph("-RELATORIO DE NOTAS"));
            doc.add(new Paragraph("Aluno: " + aluno.getNome()));
            doc.add(new Paragraph("Curso: " + aluno.getCurso()));
            doc.add(new Paragraph("\n"));

            for (int i = 0; i < sistema.mostraSemestres().size(); i++) {
                // Adding cells to the table
                Semestre s = sistema.mostraSemestres().get(i);
                sistema.setIdSemestre(s.getId());
                doc.add(new Paragraph(s.toString()));

                for (int j = 0; j < sistema.mostraDisciplinas().size(); j++) {
                    Disciplina d = sistema.mostraDisciplinas().get(j);
                    sistema.setIdDisciplina(d.getId());
                    doc.add(
                            new Paragraph(d.toString()));
                    doc.add(
                            new Paragraph("" + sistema.calcularNota()));
                }

                doc.add(new Paragraph("\n"));

            }

            // Closing the document
            doc.close();
        } catch (FileNotFoundException | SelectException | InsertException e) {
            e.printStackTrace();
        
    }    }//GEN-LAST:event_jButton13ActionPerformed

